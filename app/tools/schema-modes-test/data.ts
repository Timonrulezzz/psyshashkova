export type ModeResult = {
  mode: Mode;
  rawScore: number;       // среднее по прямым ответам (1-6)
  level: LevelKey;
  levelIndex: number;     // 0..5
};

export function computeModeScore(
  mode: Mode,
  answers: Record<number, number>
): ModeResult {
  // Считаем среднее по тем вопросам режима, на которые есть ответы
  const validAnswers = mode.questionIndexes
    .map((qi) => answers[qi])
    .filter((v): v is number => typeof v === 'number');

  const rawScore =
    validAnswers.length > 0
      ? validAnswers.reduce((s, v) => s + v, 0) / validAnswers.length
      : 0;

  // Определяем уровень
  const [t1, t2, t3, t4, t5] = mode.normBoundaries;
  let levelIndex = 0;
  if (rawScore >= t5) levelIndex = 5;
  else if (rawScore >= t4) levelIndex = 4;
  else if (rawScore >= t3) levelIndex = 3;
  else if (rawScore >= t2) levelIndex = 2;
  else if (rawScore >= t1) levelIndex = 1;
  else levelIndex = 0;

  const levelKeys: LevelKey[] = ['low', 'reduced', 'moderate', 'elevated', 'high', 'veryHigh'];
  const level = levelKeys[levelIndex];

  return { mode, rawScore, level, levelIndex };
}

export function computeAllResults(answers: Record<number, number>): ModeResult[] {
  return modes.map((m) => computeModeScore(m, answers));
}

// Топ-N дисфункциональных режимов по выраженности
export function getTopDysfunctional(results: ModeResult[], n = 3): ModeResult[] {
  return results
    .filter((r) => !r.mode.isHealthy)
    .sort((a, b) => b.rawScore - a.rawScore)
    .slice(0, n);
}

// Уровень здоровых режимов (среднее по Счастливому Ребёнку и Здоровому Взрослому)
export function getHealthyLevel(results: ModeResult[]): {
  averageScore: number;
  level: LevelKey;
} {
  const healthy = results.filter((r) => r.mode.isHealthy);
  if (healthy.length === 0) return { averageScore: 0, level: 'low' };
  const avg = healthy.reduce((s, r) => s + r.rawScore, 0) / healthy.length;
  // Используем нормативы Здорового Взрослого как референс
  const ha = modes.find((m) => m.key === 'healthyAdult')!;
  const [t1, t2, t3, t4, t5] = ha.normBoundaries;
  let levelIndex = 0;
  if (avg >= t5) levelIndex = 5;
  else if (avg >= t4) levelIndex = 4;
  else if (avg >= t3) levelIndex = 3;
  else if (avg >= t2) levelIndex = 2;
  else if (avg >= t1) levelIndex = 1;
  const levelKeys: LevelKey[] = ['low', 'reduced', 'moderate', 'elevated', 'high', 'veryHigh'];
  return { averageScore: avg, level: levelKeys[levelIndex] };
}

