export function groupResultsByCategory(
  results: ModeResult[]
): Record<ModeCategory, ModeResult[]> {
  const grouped: Record<ModeCategory, ModeResult[]> = {
    child: [],
    coping: [],
    parent: [],
    healthy: [],
  };
  results.forEach((r) => {
    grouped[r.mode.category].push(r);
  });
  // Счастливого Ребёнка по логике лекции относим к категории child, но он isHealthy=true
  // Здоровый Взрослый — отдельная категория healthy
  return grouped;
}